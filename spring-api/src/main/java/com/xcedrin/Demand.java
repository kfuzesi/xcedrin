package com.xcedrin;

public class Demand {
	String track;
	String req_title;
	String specialty;
	String location;
	String num_tickets;
	String onboarding_quarter;
	String bu;
	String subgroup;
	
	public String getTrack() {
		return track;
	}
	public void setTrack(String track) {
		this.track = track;
	}
	public String getReq_title() {
		return req_title;
	}
	public void setReq_title(String req_title) {
		this.req_title = req_title;
	}
	public String getSpecialty() {
		return specialty;
	}
	public void setSpecialty(String specialty) {
		this.specialty = specialty;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getNum_tickets() {
		return num_tickets;
	}
	public void setNum_tickets(String num_tickets) {
		this.num_tickets = num_tickets;
	}
	public String getOnboarding_quarter() {
		return onboarding_quarter;
	}
	public void setOnboarding_quarter(String onboarding_quarter) {
		this.onboarding_quarter = onboarding_quarter;
	}
	public String getSubgroup() {
		return subgroup;
	}
	public void setSubgroup(String subgroup) {
		this.subgroup = subgroup;
	}
	public String getBu() {
		return bu;
	}
	public void setBu(String bu) {
		this.bu = bu;
	}
}
